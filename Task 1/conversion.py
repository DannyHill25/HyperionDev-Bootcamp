#For this task, we will work on converting different variables into different data types.
#This will be done via 'Casting'. Usually Python can identify the Data Type directly, but we can manipulate this at will.
#For this task, we will declare for different number variables, cast them into different data types and print them
#to see what happens based on their data type.

#Step1: Declare the initial variables
num1 = 99.23
num2 = 23
num3 = 150
string1 = "100"

#Step2: Convert each variable to the designated Data Type by Casting said variable and overwritten it's stored value
num1 = int(num1)
num2 = float(num2)
num3 = str(num3)
string1 = int(string1)

#Step3: Print the variables to see the results of the Casting
print(num1)
print(num2)
print(num3)
print(string1)